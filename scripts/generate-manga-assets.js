/**
 * Scans src/manga for series folders and generates data.ts and asset-map.ts.
 * CBZ-driven: finds all .cbz, looks for matching .webp cover (same base name).
 * Run: node scripts/generate-manga-assets.js
 */

const fs = require('fs');
const path = require('path');

const MANGA_DIR = path.join(__dirname, '..', 'src', 'manga');
const COVERS_SUBFOLDER = 'manga covers';
const SKIP = new Set(['types.ts', 'data.ts', 'asset-map.ts']);

function slug(name) {
  return name.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '');
}

function scanSeries() {
  const entries = fs.readdirSync(MANGA_DIR, { withFileTypes: true });
  const out = [];

  for (const ent of entries) {
    if (!ent.isDirectory() || SKIP.has(ent.name)) continue;
    const folderPath = path.join(MANGA_DIR, ent.name);
    const files = fs.readdirSync(folderPath);
    const cbzs = files.filter((f) => f.endsWith('.cbz'));
    const rootWebps = files.filter((f) => f.endsWith('.webp'));

    let coverDirFiles = [];
    const coversPath = path.join(folderPath, COVERS_SUBFOLDER);
    if (fs.existsSync(coversPath) && fs.statSync(coversPath).isDirectory()) {
      coverDirFiles = fs.readdirSync(coversPath);
    }
    const coverWebps = coverDirFiles.filter((f) => f.endsWith('.webp'));

    const volumes = [];
    for (const cbzName of cbzs) {
      const base = cbzName.replace(/\.cbz$/, '');
      const webpName = base + '.webp';
      let match = rootWebps.find((w) => w.toLowerCase() === webpName.toLowerCase());
      let coverRequirePath = match || '';
      if (!match) {
        match = coverWebps.find((w) => w.toLowerCase() === webpName.toLowerCase());
        if (match) coverRequirePath = COVERS_SUBFOLDER + '/' + match;
      }
      if (!match) console.warn('  No cover for', cbzName, '- using placeholder');
      const title = base.replace(/^[^v]*\s*v?(\d+).*$/i, 'v$1').trim() || base;
      volumes.push({
        coverFilename: match || '',
        title,
        cbzFilename: cbzName,
        coverRequirePath,
      });
    }
    volumes.sort((a, b) => a.cbzFilename.localeCompare(b.cbzFilename));
    out.push({
      id: slug(ent.name),
      name: ent.name,
      folder: ent.name,
      volumes,
    });
  }
  return out.sort((a, b) => a.name.localeCompare(b.name));
}

function generateDataTs(seriesList) {
  const forExport = seriesList.map((s) => ({
    ...s,
    volumes: s.volumes.map(({ coverFilename, title, cbzFilename }) => ({
      coverFilename,
      title,
      cbzFilename,
    })),
  }));
  const json = JSON.stringify(forExport, null, 2).replace(/"([^"]+)":/g, '$1:');
  const content = `import type { MangaManifest } from './types';

export const mangaManifest: MangaManifest = {
  series: ${json},
};
`;
  fs.writeFileSync(path.join(MANGA_DIR, 'data.ts'), content, 'utf8');
  console.log('Wrote src/manga/data.ts');
}

function generateAssetMap(seriesList) {
  const coverLines = [];
  const cbzLines = [];
  for (const s of seriesList) {
    for (const v of s.volumes) {
      if (v.coverFilename && v.coverRequirePath) {
        const cKey = `'${s.folder}|${v.coverFilename}'`;
        const reqPath = (s.folder + '/' + v.coverRequirePath).replace(/\\/g, '/');
        coverLines.push(`  ${cKey}: require('./${reqPath}'),`);
      }
      const zKey = `'${s.folder}|${v.cbzFilename}'`;
      cbzLines.push(`  ${zKey}: require('./${s.folder}/${v.cbzFilename}'),`);
    }
  }
  const content = `/**
 * Auto-generated by scripts/generate-manga-assets.js
 */
import type { ImageSourcePropType } from 'react-native';

type AssetMap = Record<string, ImageSourcePropType>;
type CbzMap = Record<string, number>;

const COVERS: AssetMap = {
${coverLines.join('\n')}
};

const CBZ: CbzMap = {
${cbzLines.join('\n')}
};

function key(folder: string, filename: string) {
  return \`\${folder}|\${filename}\`;
}

export function getMangaCoverSource(folder: string, coverFilename: string): ImageSourcePropType | null {
  return COVERS[key(folder, coverFilename)] ?? null;
}

export function getCbzRequire(folder: string, cbzFilename: string): number | null {
  return CBZ[key(folder, cbzFilename)] ?? null;
}
`;
  fs.writeFileSync(path.join(MANGA_DIR, 'asset-map.ts'), content, 'utf8');
  console.log('Wrote src/manga/asset-map.ts');
}

function main() {
  if (!fs.existsSync(MANGA_DIR)) {
    console.error('Not found:', MANGA_DIR);
    process.exit(1);
  }
  const seriesList = scanSeries();
  console.log('Manga series:', seriesList.length);
  seriesList.forEach((s) => console.log('  -', s.name, '(' + s.volumes.length + ' volumes)'));
  generateDataTs(seriesList);
  generateAssetMap(seriesList);
}

main();
